    private void MostrarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarBotonActionPerformed
        // TODO add your handling code here:
        
        TituloConfirmar.setText(this.parent.getGestor().getLibroComprado().getTitulo());
          int multiplicador=1;
        switch (this.parent.getGestor().getMesesconsulta()){
            case "1": multiplicador=1; break;

            case "2":multiplicador=2; break;
                
            case "3":multiplicador=3; break;

            case "4":multiplicador=4; break;
                
            case "5":multiplicador=5; break;

            case "6":multiplicador=6; break;
                
            case "7":multiplicador=7; break;

            case "8":multiplicador=8; break;
                
            case "9":multiplicador=9; break;

            case "10":multiplicador=10; break;
                
            case "11":multiplicador=11; break;

            case "12":multiplicador=12; break;

        }

        double preciofinal = (this.parent.getGestor().getLibroComprado().getPrecioMensual()) *( multiplicador);
        this.parent.getGestor().setPreciotrasdescuento(preciofinal);
        PrecioConfirmar.setText("" + preciofinal + "");

    }//GEN-LAST:event_MostrarBotonActionPerformed

